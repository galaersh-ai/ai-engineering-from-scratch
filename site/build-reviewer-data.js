#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const phasesDir = path.join(repoRoot, 'phases');
const outputPath = path.join(__dirname, 'reviewer-data.js');

function readIfExists(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf8');
  } catch (_) {
    return '';
  }
}

function build() {
  const payload = {};
  if (!fs.existsSync(phasesDir)) {
    throw new Error(`Missing phases directory: ${phasesDir}`);
  }

  for (const phaseName of fs.readdirSync(phasesDir).sort()) {
    if (!/^\d{2}-/.test(phaseName)) continue;
    const phasePath = path.join(phasesDir, phaseName);
    if (!fs.statSync(phasePath).isDirectory()) continue;

    for (const lessonName of fs.readdirSync(phasePath).sort()) {
      if (!/^\d{2}-/.test(lessonName)) continue;
      const lessonPath = path.join(phasePath, lessonName);
      if (!fs.statSync(lessonPath).isDirectory()) continue;

      const rel = `phases/${phaseName}/${lessonName}`;
      const en = readIfExists(path.join(lessonPath, 'docs', 'en.md'));
      const ru = readIfExists(path.join(lessonPath, 'docs', 'ru.md'));
      if (!en && !ru) continue;
      payload[rel] = { en, ru };
    }
  }

  const output = `// Auto-generated by build-reviewer-data.js. Do not edit manually.
// Bundled lesson markdown lets reviewer.html work from file:// URLs.
window.REVIEWER_MARKDOWN = ${JSON.stringify(payload)};
`;
  fs.writeFileSync(outputPath, output, 'utf8');
  console.log(`Generated ${outputPath} with ${Object.keys(payload).length} lessons`);
}

build();
